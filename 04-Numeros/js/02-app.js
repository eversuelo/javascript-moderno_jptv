//Crreacion de Numeros
const numero1 = 30;
const numero2 = 20;
console.log(`Los numeros ${numero1} y  ${numero2} tienen las siguientes operaciones`);
let resultado;
//Suma
resultado = numero1 + numero2;
console.log(resultado);
//Resta 
resultado = numero1 - numero2;
console.log(resultado);
//Multiplicacion
resultado = numero1 * numero2;
console.log(resultado);
//Division
resultado = numero1 / numero2;
console.log(resultado);
//Modulo
resultado = numero1 % numero2;

console.log(resultado);